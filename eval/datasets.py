"""Load RAGTruth and HaluEval into ONE common schema (`EvalExample`).

The whole project is "a measured hallucination reduction," so the data is the
foundation. Both benchmarks give us (context, response, label) triples where
the label is a GROUNDEDNESS / FAITHFULNESS judgement — is the response
supported by its context? — NOT a world-truth judgement. We unify them so the
verifier and the metrics never have to know which benchmark a row came from.

Where the labels come from:
  RAGTruth  — responses generated by real LLMs (GPT-4, Llama-2, Mistral, ...)
              over retrieved/source context, with human-annotated hallucination
              SPANS. A response is hallucinated iff it has >=1 annotated span.
              This is the primary, on-target RAG hallucination corpus.
  HaluEval  — each row pairs one faithful answer with one hallucinated answer
              over the same knowledge. We emit both, so the set is balanced
              50/50 by construction — which doubles as a correctness check on
              our label mapping (see eval/baseline.py).

Files are downloaded once into data/benchmarks/ and cached.
"""

import json
from dataclasses import dataclass, field
from pathlib import Path

import requests

DATA_DIR = Path("data/benchmarks")

RAGTRUTH_SOURCE_URL = "https://raw.githubusercontent.com/ParticleMedia/RAGTruth/main/dataset/source_info.jsonl"
RAGTRUTH_RESPONSE_URL = "https://raw.githubusercontent.com/ParticleMedia/RAGTruth/main/dataset/response.jsonl"
HALUEVAL_QA_URL = "https://raw.githubusercontent.com/RUCAIBox/HaluEval/main/data/qa_data.json"


@dataclass
class EvalExample:
    """One verification instance, common to every benchmark.

    context  = the grounding premise (retrieved passages / knowledge).
    response = the text whose groundedness we judge (the NLI hypothesis).
    hallucinated = answer-level ground truth: is `response` unsupported by
                   `context`? We start COARSE (answer-level); `spans` carries
                   RAGTruth's finer labels for the later claim/span-level work.
    """

    id: str
    dataset: str  # "ragtruth" | "halueval"
    task_type: str  # ragtruth: "QA"|"Summary"|"Data2txt" ; halueval: "qa"
    context: str
    response: str
    hallucinated: bool
    question: str | None = None
    split: str | None = None  # ragtruth: "train"|"test"
    spans: list = field(default_factory=list)  # ragtruth span annotations
    meta: dict = field(default_factory=dict)


def _download(url: str, dest: Path) -> Path:
    """Fetch `url` to `dest` once; reuse the cached file thereafter."""
    if dest.exists():
        return dest
    dest.parent.mkdir(parents=True, exist_ok=True)
    resp = requests.get(url, timeout=180)
    resp.raise_for_status()
    dest.write_bytes(resp.content)
    return dest


def _read_jsonl(path: Path):
    """Yield parsed objects from a JSON-lines file, skipping blank lines."""
    for line in path.read_text(encoding="utf-8").splitlines():
        if line.strip():
            yield json.loads(line)


def load_ragtruth(split: str | None = None, include_truncated: bool = True) -> list[EvalExample]:
    """Load RAGTruth, joining responses to their source context on source_id.

    split: "train" / "test" / None (all). include_truncated keeps responses
    the annotators marked "truncated" (incomplete generations); set False to
    drop them if they prove noisy.
    """
    src_path = _download(RAGTRUTH_SOURCE_URL, DATA_DIR / "ragtruth_source_info.jsonl")
    rsp_path = _download(RAGTRUTH_RESPONSE_URL, DATA_DIR / "ragtruth_response.jsonl")

    sources = {o["source_id"]: o for o in _read_jsonl(src_path)}

    examples: list[EvalExample] = []
    for o in _read_jsonl(rsp_path):
        if split is not None and o["split"] != split:
            continue
        if not include_truncated and o.get("quality") == "truncated":
            continue
        s = sources[o["source_id"]]

        # source_info is a plain string for QA/Summary; for Data2txt it can be
        # structured (a dict/list). Stringify so `context` is always text the
        # NLI model can read as a premise.
        ctx = s["source_info"]
        if not isinstance(ctx, str):
            ctx = json.dumps(ctx, ensure_ascii=False)

        examples.append(
            EvalExample(
                id=f"ragtruth-{o['id']}",
                dataset="ragtruth",
                task_type=s["task_type"],
                context=ctx,
                response=o["response"],
                # ANSWER-LEVEL label: any annotated span => hallucinated.
                hallucinated=len(o["labels"]) > 0,
                question=None,  # the question is embedded in s["prompt"] (kept in meta)
                split=o["split"],
                spans=o["labels"],
                meta={
                    "model": o["model"],
                    "quality": o.get("quality"),
                    "source": s.get("source"),
                    "prompt": s.get("prompt"),
                },
            )
        )
    return examples


def load_halueval_qa(limit: int | None = None) -> list[EvalExample]:
    """Load HaluEval QA. Each row -> two examples (faithful + hallucinated).

    Emitting both halves keeps the set balanced 50/50, which is exactly the
    invariant baseline.py asserts to prove the label mapping is correct.
    """
    path = _download(HALUEVAL_QA_URL, DATA_DIR / "halueval_qa_data.json")

    examples: list[EvalExample] = []
    for i, o in enumerate(_read_jsonl(path)):
        base = f"halueval-qa-{i}"
        examples.append(
            EvalExample(
                id=f"{base}-pos",
                dataset="halueval",
                task_type="qa",
                context=o["knowledge"],
                response=o["right_answer"],
                hallucinated=False,
                question=o["question"],
                meta={"variant": "right_answer"},
            )
        )
        examples.append(
            EvalExample(
                id=f"{base}-neg",
                dataset="halueval",
                task_type="qa",
                context=o["knowledge"],
                response=o["hallucinated_answer"],
                hallucinated=True,
                question=o["question"],
                meta={"variant": "hallucinated_answer"},
            )
        )
        if limit is not None and len(examples) >= limit:
            break
    return examples


def load(name: str, **kwargs) -> list[EvalExample]:
    """Dispatch by benchmark name into the common schema."""
    if name == "ragtruth":
        return load_ragtruth(**kwargs)
    if name == "halueval":
        return load_halueval_qa(**kwargs)
    raise ValueError(f"unknown dataset {name!r} (expected 'ragtruth' or 'halueval')")
